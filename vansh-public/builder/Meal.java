public class Meal {

    private String curry;

    private boolean rice;

    private boolean naan;

    private boolean papadum;

    public String getCurry() {
        return curry;
    }
    
    public boolean getRice() {
        return rice;
    }

    public boolean getNaan() {
        return naan;
    }

    public boolean getPapadum() {
        return papadum;
    }

    public void setCurry(String curry) {
        this.curry = curry;
    }

    public void setRice(boolean rice) {
        this.rice = rice;
    }

    public void setNaan(boolean naan) {
        this.naan = naan;
    }

    public void setPapadum(boolean papadum) {
        this.papadum = papadum;
    }

    
}
